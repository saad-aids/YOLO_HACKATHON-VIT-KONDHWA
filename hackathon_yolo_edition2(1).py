class DINOv2SegModel(nn.Module):
    def __init__(self, num_classes=NUM_CLASSES):
        super().__init__()
        self.backbone = torch.hub.load(
            'facebookresearch/dinov2', 'dinov2_vits14')
        for p in self.backbone.parameters():
            p.requires_grad = False

        self.head = nn.Sequential(
            nn.ConvTranspose2d(384, 256, kernel_size=2, stride=2),
            nn.BatchNorm2d(256), nn.GELU(),
            nn.ConvTranspose2d(256, 128, kernel_size=2, stride=2),
            nn.BatchNorm2d(128), nn.GELU(),
            nn.ConvTranspose2d(128, 64,  kernel_size=2, stride=2),
            nn.BatchNorm2d(64),  nn.GELU(),
            nn.Conv2d(64, num_classes, kernel_size=1),
        )

    def forward(self, x):
        H, W = x.shape[2], x.shape[3]
        with torch.no_grad():
            tokens = self.backbone.get_intermediate_layers(x, n=1)[0]
        B, N, C = tokens.shape
        g    = int(N ** 0.5)
        feat = tokens.permute(0,2,1).reshape(B, C, g, g)
        logits = self.head(feat)
        return F.interpolate(logits, size=(H,W),
                             mode='bilinear', align_corners=False)

