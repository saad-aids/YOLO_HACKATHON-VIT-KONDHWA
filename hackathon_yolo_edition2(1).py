def tta_predict_batch(model, imgs_tensor, device):
    with torch.no_grad():
        p1 = F.softmax(model(imgs_tensor.to(device)), dim=1)
        p2 = F.softmax(model(imgs_tensor.flip(dims=[-1]).to(device)), dim=1)
        p2 = p2.flip(dims=[-1])
    return ((p1 + p2) / 2).argmax(dim=1).cpu()

