def compute_iou(preds, masks, num_classes=NUM_CLASSES):
    preds = preds.cpu().numpy()
    masks = masks.cpu().numpy()
    per_class, valid = [], []
    for c in range(num_classes):
        gt_c  = (masks == c) & (masks != 255)
        pr_c  = (preds == c) & (masks != 255)
        inter = (gt_c & pr_c).sum()
        union = (gt_c | pr_c).sum()
        if union == 0:
            per_class.append(None)
        else:
            iou = inter / union
            per_class.append(iou)
            valid.append(iou)
    return float(np.mean(valid)) if valid else 0.0, per_class

