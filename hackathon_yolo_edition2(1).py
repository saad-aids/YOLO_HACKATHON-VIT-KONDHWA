import cv2, os, torch, torch.nn.functional as F
import numpy as np, albumentations as A
from albumentations.pytorch import ToTensorV2
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches

def tta_predict(model, img_rgb, device, size=504):
    """6 predictions = original + hflip + 3 scales each with flip"""

    model.eval()
    all_probs = []

    scales = [0.75, 1.0, 1.25]

    for scale in scales:
        h = w = int(size * scale)
        # clamp to safe size for DINOv2 (multiple of 14)
        h = (h // 14) * 14
        w = (w // 14) * 14

        tf = A.Compose([
            A.Resize(h, w),
            A.Normalize(mean=(0.485,0.456,0.406),
                       std=(0.229,0.224,0.225)),
            ToTensorV2()
        ])

        # Original
        t = tf(image=img_rgb)['image'].unsqueeze(0).to(device)
        with torch.no_grad():
            p = F.softmax(model(t), dim=1)
            p = F.interpolate(p, size=(size,size), mode='bilinear',
                            align_corners=False)
        all_probs.append(p.cpu())

        # Horizontal flip
        img_flip = cv2.flip(img_rgb, 1)
        t_f = tf(image=img_flip)['image'].unsqueeze(0).to(device)
        with torch.no_grad():
            p_f = F.softmax(model(t_f), dim=1)
            p_f = F.interpolate(p_f, size=(size,size), mode='bilinear',
                              align_corners=False)
            p_f = p_f.flip(dims=[-1])  # flip back
        all_probs.append(p_f.cpu())

    # Average all 6 predictions
    avg_prob = torch.stack(all_probs).mean(0)
    return avg_prob.argmax(dim=1).squeeze().numpy()

